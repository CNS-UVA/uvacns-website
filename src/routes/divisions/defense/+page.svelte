<h1 class="typography">Cyber Defense Division</h1>
<p class="text-muted-foreground text-center text-xl leading-7 [&:not(:first-child)]:mt-6">
	The Cyber Defense Division protects computers, networks, and applications from cyber attacks
	through hardening and incident response.
</p>
<img class="m-auto w-3xl mt-10" src="/images/LuluCharlotte2024Quals.jpg" alt="Cyber Defense Division members in competition" />
<div class="typography">
	<div>
		<h2>Achievements</h2>
		<p>
			The Cyber Defense Division represents UVA in the <a href="https://nccdc.org"
				>Collegiate Cyber Defense Competition</a
			>, where it is one of the most successful teams in CCDC history. Its accolades include:
		</p>
		<ul>
			<li><strong>Three</strong> national championships (2018-20)</li>
			<li><strong>Four</strong> national top-three placements (2018-20, 2025)</li>
			<li><strong>Seven</strong> national top-ten placements (2018-20, 2022-25)</li>
			<li><strong>Five</strong> Mid-Atlantic championships (2018-19, 2023-25)</li>
		</ul>
		<p>
			In the 2025 season, UVA placed second at the National Collegiate Cyber Defense Competition,
			which was held virtually from April 25-27, 2025.
		</p>
		<p>
			In addition to CCDC, the Cyber Defense Division sends teams to various blue-team competitions
			around the country.
		</p>
